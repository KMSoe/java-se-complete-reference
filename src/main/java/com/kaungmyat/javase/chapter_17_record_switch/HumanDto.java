package com.kaungmyat.javase.chapter_17_record_switch;

public class HumanDto {
    String name;
    int age;

    public HumanDto(String name, int age) {
        this.name = name;
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }
}
